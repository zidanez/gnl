#include "libft.h"

int		ft_ilen(int c)
{
	int res;

	res = 0;
	if (c == 0)
		res = 1;
	if (c != 0)
	{
		if (c < 0)
		{
			if (c == -2147483648)
				return (11);
			res++;
			c = c * -1;
		}
		while (c != 0)
		{
			c = c / 10;
			res++;
		}
	}
	return (res);
}
